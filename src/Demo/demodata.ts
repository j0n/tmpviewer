export const convertDemoRoomsToPixPolygons = (rooms: IDemoRoom[]): IPixPolygon[] => {
  return rooms.map(room => {
    return {
      id: room.globalId,
      points: room.polygon,
      fillColor: "#fff",
      strokeColor: "#000",
      strokeWidth: "1px",
      text: {
        value: `${room.name} - ${room.longName}`,
        color: "#000",
        fontSize: "15px",
      },
      tooltip: `${room.name} - ${room.longName}`,
      isMoveable: false
    }
  });
};

export const convertRoomGridsToPixPaths = (rooms: IDemoRoomWithDevices[]): IPixPath[] => {
  return rooms.map(room => {
    return {
      id: room._id,
      points: room.grid,
      strokeColor: "#000",
      strokeWidth: "1px",
    }
  })
}

export const convertRoomDevicesToPixPolygons = (rooms: IDemoRoomWithDevices[]): IPixPolygon[] => {
  return rooms.map(room => {
    return room.modifiedMlResults.map(device => {
      return {
        id: device.id,
        points: device.bounding_box,
        fillColor: "#f00",
        strokeColor: "#000",
        strokeWidth: "1px",
        text: {
          value: device.comp_code,
          color: "#000",
          fontSize: "15px",
        },
        tooltip: device.comp_code,
        isMoveable: true
      }
    })
  }).flat();
}
